                <p class="font-semibold">An interdisciplinary team led by University of Wisconsin-Madison scientists has
                    developed a new
                    technique that could help farmers extract useful nutrients such as ammonia and potassium from
                    livestock manure to efficiently make fertilizer and other useful chemical products. While the
                    strategy still needs to be scaled up beyond a proof-of-concept stage, the group's preliminary
                    analyses show it could offer considerable benefits by cutting water and air pollution while
                    simultaneously creating products that farmers could use or sell.</p>
                <p>Manure stinks in part because it contains ammonia, one of the more than 300 compounds that contribute
                    to its odor.
                    <br>
                    The pungent gas is not only a harmful air pollutant but can turn into the greenhouse gas nitrous
                    oxide and water-polluting nitrate.</p>
                <p>

                    Ammonia is also the foundation of many nitrogen fertilizers that have fueled modern crop production.
                </p><br>

                <p>
                    The industrial method for making ammonia for nitrogen-based fertilizers, the Haber-Bosch process,
                    consumes a lot of energy and emits hundreds of millions of tons of greenhouse gasses every year.
                </p><br>

                <p>
                    Although manure itself can be used as fertilizer, doing so can be costly, logistically challenging
                    and
                    has environmental drawbacks.
                </p><br>

                <p>
                    So, researchers around the world are hunting for strategies to efficiently recover ammonia from
                    manure,
                    creating more concentrated and valuable fertilizers that are greener and more affordable to
                    transport.
                </p><br>

                <p>
                    Among these strategies are chemical processes driven by electricity, which is becoming increasingly
                    inexpensive in many rural communities thanks to growing solar and wind power generation.
                </p><br>

                <p>
                    However, most electrochemical techniques in development are not yet practical, mainly because they
                    consume a lot of energy and aren't efficient enough at pulling dissolved ammonia (in the form of
                    ammonium ions) out of manure.
                </p><br>

                <p>
                    This new technique, described in a Dec. 8 paper published in the journal Nature Sustainability,
                    relies
                    on a specially designed electrode, like those used for batteries, that targets ammonium ions.
                </p><br>

                <p>
                    The technique involves a nickel-based electrode that is placed directly into the manure wastewater.
                </p><br>

                <p>
                    As organic matter in the manure naturally gets oxidized by the electrode, the ammonium, as well as
                    potassium ions, within the wastewater are selectively driven into and captured by the electrode.
                </p><br>

                <p>
                    The strategy does not end with simply removing the nutrients from the wastewater.
                </p><br>

                <p>
                    In an innovative step that could help make the process even more attractive, the nutrient-loaded
                    electrode is then placed into a device that uses electricity to release the recovered ammonium and
                    potassium ions, which can then be used to make nitrogen and potassium-based fertilizers, and
                    simultaneously produce other useful chemical products.
                </p><br>

                <p>
                    These could include hydrogen fuel or hydrogen peroxide, which is commonly used for disinfection.
                </p><br>

                <p>
                    "We got lucky because nature does a lot of the work for us," says Song Jin, a UW-Madison professor
                    of
                    chemistry who led the work along with doctoral candidate Rui Wang and civil and environmental
                    engineering Professor Mohan Qin.
